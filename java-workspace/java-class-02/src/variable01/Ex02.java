package variable01;

public class Ex02 {
	public static void main(String[] args) {
		
		int a; //정수 타입 변수
		a = 10; // 선언 후 저장 : 초기화
		// = 은 오른쪽의 값을 왼쪽변수에 저장한다.
		// int a = 20; // 에러
		// 같은 이름의 변수가 2개가 있으면 프로그램에서는 어떤 변수를 골라야 하는지 모름
		
		int b;
		b = 20;
		
		int c;
		c = 30;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		int aa = 40; // 선언과 동시에 초기화
		int bb = 50;
		int cc = 60;
		
		System.out.println(aa);
		System.out.println(bb);
		System.out.println(cc);
		
	}
}
