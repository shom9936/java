package folder;

public class Sample {

	public static void main(String[] args) {

		
		System.out.println("문자열입니다.");
		System.out.println("문자열 안에 내용은 그대로 출력이 됩니다.");
		System.out.println("안녕하세요");
		//()안에 내용을 출력하고 줄바꿈
		
		System.out.print("안녕");
		System.out.print("하세요");
		//()안에 내용을 출력하고 줄바꾸지 않음
		System.out.println();
		System.out.println("1+5");
		//""안에 넣은 내용은 그대로 출력
		System.out.println(1+5);
		//()안에 값을 계산 후 6을 출력
		
		
	}

}
